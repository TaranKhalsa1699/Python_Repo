## Looping through index numbers and List
List = ["Apple", "Mango", "Banana"]
for i in range(len(List)):
    print(List[i])

for x in List:    
  print(x)

# This is List Comprehension ##
[print(x) for x in List] 

#while loop to show or display List##
i = 0
while i < len(List):
    print(List[i])
    i = i + 1

